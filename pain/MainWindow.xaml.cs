		private void Button_Click_1(object sender, RoutedEventArgs e)
		{
			string errors = "";

			var buttons = FindVisualChilds<Button>(this);
			buttons = buttons.Where(b => b.Content.ToString().Length == 1);

			if(buttons.Take(10).Where(b=>b.Content.ToString() == "_").Count() >= 9) 
			{
				errors += "FIRST NAME MUST HAVE AT LEAST 2 CHARACTERS!";
			}

			if (buttons.Skip(10).Take(10).Where(b => b.Content.ToString() == "_").Count() >= 9)
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "LAST NAME MUST HAVE AT LEAST 2 CHARACTERS!";
			}

			if(buttons.Take(10).Where(b => Char.IsUpper(b.Content.ToString().ToCharArray()[0])).Count() > 1)
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "FIRST NAME CAN HAVE ONLY ONE CAPITAL LETTER!";
			}

			if (buttons.Skip(10).Take(10).Where(b => Char.IsUpper(b.Content.ToString().ToCharArray()[0])).Count() > 1)
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "LAST NAME CAN HAVE ONLY ONE CAPITAL LETTER!";
			}

			int m;
			int d;
			int y;
			if (!int.TryParse(monthSlider.Value.ToString(), out m))
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "BIRTH MONTH IS NOT VALID!";
			}
			else
			{
				if(m > 12)
				{
					if (!string.IsNullOrEmpty(errors))
					{
						errors += "\r\n";
					}
					errors += "BIRTH MONTH VALUE IS TOO HIGH!";
				}
			}

			if (!int.TryParse(daySlider.Value.ToString(), out d))
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "BIRTH DAY IS NOT VALID!";
			}
			else
			{
				if (d > 31)
				{
					if (!string.IsNullOrEmpty(errors))
					{
						errors += "\r\n";
					}
					errors += "BIRTH DAY VALUE IS TOO HIGH!";
				}
			}

			if (!int.TryParse(yearSlider.Value.ToString(), out y))
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "BIRTH YEAR IS NOT VALID!";
			}
			else
			{
				if (y > 2022)
				{
					if (!string.IsNullOrEmpty(errors))
					{
						errors += "\r\n";
					}
					errors += "BIRTH YEAR VALUE IS TOO HIGH!";
				}
			}

			if(m == 1 && d == 1 && y == 1)
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "BIRTH DATE NOT SET!";
			}

			if (Convert.ToInt32(txtNum.Text) < 9999999)
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "PHONE NUMBER MUST HAVE AT LEAST 8 DIGITS!";
			}

			List<string> forbiddenChars = new List<string>()
			{
				"\"","(",")",",",":",";","<",">","@","[","\\","]", " "
			};

			if (string.IsNullOrEmpty(emailBox.Password) || domainComboBox.SelectedItem == null)
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "E-MAIL FIELD IS REQUIRED!";
			}

			if (emailBox.Password.ToCharArray().ToList().Select(c=>c.ToString()).Intersect(forbiddenChars).Any())
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "E-MAIL CONTAINS INVALID CHARACTERS!";
			}

			if (string.IsNullOrEmpty(addressField.Text))
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "ADDRESS FIELD IS REQUIRED!";
			}

			Regex r = new Regex(@"^\d\d \d\d \d\d (N|S);\d\d \d\d \d\d (W|E);$");

			if (!r.IsMatch(addressField.Text))
			{
				if (!string.IsNullOrEmpty(errors))
				{
					errors += "\r\n";
				}
				errors += "ADDRESS MUST FOLLOW DMS COORDINATES FORMAT\r\n[DD MM SS N|S;DD MM SS W|E]!";
			}

			errorDisplay.Text = errors;
		}

		public static IEnumerable<T> FindVisualChilds<T>(DependencyObject depObj) where T : DependencyObject
		{
			if (depObj == null) yield return (T)Enumerable.Empty<T>();
			for (int i = 0; i < VisualTreeHelper.GetChildrenCount(depObj); i++)
			{
				DependencyObject ithChild = VisualTreeHelper.GetChild(depObj, i);
				if (ithChild == null) continue;
				if (ithChild is T t) yield return t;
				foreach (T childOfChild in FindVisualChilds<T>(ithChild)) yield return childOfChild;
			}
		}
	}
